package com.example.QuickReactionMJ;

public class VisitData {

    private String visit;
    private String time;

    public VisitData(String visit, String time) {
        this.visit = visit;
        this.time = time;
    }

    public String getVisit() {
        return this.visit;
    }

    public String getTime() {
        return this.time;
    }
}
